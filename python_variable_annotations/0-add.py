#!/usr/bin/env python3
"""module"""

def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns their sum.

    Args:
        a (float): The first floating-point number.
        b (float): The second floating-point number.

    Returns:
        float: The sum of the two input numbers.
    """
    return a + b
